    //método para excluir uma os
    private void excluir_os() {
        int confirma = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir esta OS?", "Atenção", JOptionPane.YES_OPTION);
        if (confirma == JOptionPane.YES_OPTION) {
            String sql = "delete from tbos where os=?";
            try {
                pst = conexao.prepareStatement(sql);
                pst.setString(1, txtOs.getText());
                int apagado = pst.executeUpdate();
                if (apagado>0) {
                    JOptionPane.showMessageDialog(null, "OS Apagada Com Sucesso!!!");
                    txtOs.setText(null);
                    txtData.setText(null);
                    txtCliId.setText(null);
                    txtOsEquip.setText(null);
                    txtOsServ.setText(null);
                    txtOsDef.setText(null);
                    txtOsTec.setText(null);
                    txtOsValor.setText(null);
                    //habilitar os objetos
                    btnOsAdd.setEnabled(true);
                    txtCliPesquisar.setEnabled(true);
                    tblClientes.setVisible(true);
                    
                } else {
                    JOptionPane.showMessageDialog(null,"Erro ao apagar OS!!!");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }

        } else {
        }
    }
    
    //método para imprimir uam os
    private void imprimir_os(){
            //imprimindo uma os
        int confirma = JOptionPane.showConfirmDialog(null,"Confirma a impressão desta O.S?", "Atenção",JOptionPane.YES_OPTION);
        if (confirma==JOptionPane.YES_OPTION) {
            //imprimindo relatório com framework JasperReporter
            try {
                //usando a classe HashMap para criar um filtro
                HashMap filtro = new HashMap();
                filtro.put("os",Integer.parseInt(txtOs.getText()));
                //usandoa classe jasper print
                JasperPrint print = JasperFillManager.fillReport("C:/Users/pedro/Documents/NetBeansProjects/prjinfox/build/classes/br/com/infox/reports/OsEspecificada.jasper",filtro,conexao);
                // a linha abaixo exibe  relatório atraves da classe JasperViewer
                JasperViewer.viewReport(print, false);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,e);
            }      
        }
    }
